nota = float (input("Digite a nota do aluno: ")) #float: real
#input leia
if nota >= 90:
    print ("Aluno com nota A")
    
elif nota >= 80:
    print ("Aluno com nota B")
    
elif nota >= 70:
    print ("Aluno com nota C")
    
elif nota >= 60:
    print ("Aluno com nota D")
    
else:
    print ("Aluno com nota F")